package org.variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수는 무조건 초기화를 해주어야함
        int a; //선언만
        a = 1;
        System.out.println(a);

        int b = 2; // 선언과 초기화를 같이
        System.out.println(b);

        int c =3, d= 4; //여러 변수를 선언과 초기화
        System.out.println(c);
        System.out.println(d);
    }
}

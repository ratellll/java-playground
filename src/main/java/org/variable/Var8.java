package org.variable;

public class Var8 {

    public static void main(String[] args) {
        //정수
        byte b = 127; // -128 ~ 127
        short s = 32767; //-32,768 ~ 32,767
        int i = 2147483647; //- 2,147,483 ,648~

        long l = 9999999L; //매우큼 L을 넣어줘야함

        //실수
        float f = 10.0f; // 범위가 작아 제대로 계산이안됨  실무에서 잘 안씀     s
        double d = 10.0;


    }
}
